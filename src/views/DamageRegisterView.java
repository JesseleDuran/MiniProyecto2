    private void setDispositivo(Daño damage) {
        for (int i = 0; i < dispositivos.size(); i++) {
            if(dispositivos.get(i).getId().equals(damage.getDispositivo()))
                dispositivo.setSelectedIndex(i);
        }
    }

    public void cleanFields() {
        fecha.setText("");
        costo.setText("");
        descripcion.setText("");
        resuelto.setSelected(false);
        dispositivo.removeAllItems();
    }
