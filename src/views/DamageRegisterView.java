    public void cargarDispositivos(DB4OConnection db)
    {
        dispositivo.removeAllItems();
        db.open();
        
        dispositivos = DispositivoDAO.getInstance().getAll(db);
        db.close();
        for (Dispositivo d: dispositivos) {
            dispositivo.addItem(d.getNombre());
        }
    }
    
    public void setDamage(Daño damage)
    {
        descripcion.setText(damage.getDescripcion());
        String formattedDate = new SimpleDateFormat("dd/MM/yyyy").format(damage.getFecha());
        fecha.setText(formattedDate);
        costo.setText(damage.getCosto()+"");
        resuelto.setSelected(damage.isResuelto());
        setDispositivo(damage);
        
    }
    
    public String deviceSelected()
    {
        return dispositivos.get(dispositivo.getSelectedIndex()).getId();
    }

